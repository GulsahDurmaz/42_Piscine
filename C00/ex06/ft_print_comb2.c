#include <unistd.h>

void ft_print_comb2(void)

{
	int x;
	int y;

	x = '0';
	y = '0';
	while (x <= '9')
		{
			while (y <= '9')
				{
				if(y <= '9')
					{
						write(1, &x, 2);
						write(1, &y, 2);
						write(1, " ", 1);
						y++;
					}			
				}
			x++;
		}
}	

int main()
{
	ft_print_comb2 ();
	return(0);
}
